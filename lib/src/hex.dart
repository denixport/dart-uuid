// converts hex char into 4-bit value
int hexToNibble(int char) {
  char -= 0x30;
  if (!(char >= 0 && char < hexBytes.length)) return -1;
  int nibble = hexBytes[char];
  if (char != 0 && nibble == 0) return -1;
  return nibble;
}
